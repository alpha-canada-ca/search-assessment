public class SearchPage {

	private String globalEn;
	private String globalFr;
	private String google;
	
	public String getGlobalEn() {
		return globalEn;
	}
	public void setGlobalEn(String globalEn) {
		this.globalEn = globalEn;
	}
	public String getGlobalFr() {
		return globalFr;
	}
	public void setGlobalFr(String globalFr) {
		this.globalFr = globalFr;
	}
	public String getGoogle() {
		return google;
	}
	public void setGoogle(String google) {
		this.google = google;
	}
	
}
